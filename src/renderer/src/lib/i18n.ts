const dictionaries = { ru, en };

type TranslationKey = keyof typeof ru;

function getSystemLocale(): Locale {
  const language = navigator.language.toLowerCase();
  return language.startsWith("ru") ? "ru" : "en";
}

export function getLocale(): Locale {
  const storedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (storedLanguage === "ru" || storedLanguage === "en") {
    return storedLanguage;
  }

  return getSystemLocale();
}

export function setLocalePreference(language: string) {
  if (language === "ru" || language === "en" || language === "system") {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    window.dispatchEvent(new Event(LANGUAGE_CHANGED_EVENT));
  }
}

export function t(key: TranslationKey | string, replacements?: Record<string, string | number>): string {
  const locale = getLocale();
  const dictionary = dictionaries[locale] as Record<string, string>;
  let value = dictionary[key] ?? (dictionaries.ru as Record<string, string>)[key] ?? key;

  if (replacements) {
    for (const [replacementKey, replacementValue] of Object.entries(replacements)) {
      value = value.replaceAll(`{${replacementKey}}`, String(replacementValue));
    }
  }

  return value;
}
